
package tablamultiplicarforanidado;

public class TablaMultiplicarforanidado {

    public static void main(String[] args) {
      
        int fin1 = 5, fin2=6;
        for(int x=1;x<=fin1;x++){
        
          for(int y=1;y<=fin2;y++){
              System.out.print(x*y + "\t");
          }
            System.out.println("");
        }
        
        
    }
    
}
